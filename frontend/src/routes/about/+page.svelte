<div class="about-page">
    <h1>About Me</h1>

    <p class="intro">
        Hark Horning is a world-renowned artist, software engineer, and premier dirt specialist — a combination of credentials that has yet to be replicated anywhere on earth.
    </p>

    <h2>The Art</h2>
    <p>
        Hark's paintings hang in galleries, private collections, and at least one very tasteful dentist's waiting room across the globe. Working across acrylic, oil, watercolor, pastel, and pencil, his work has been described as "breathtaking," "emotionally resonant," and once, memorably, as "the best thing I've seen all week" by a stranger at a coffee shop who may or may not have been looking at the right painting. He has won awards. Many of them. The specifics are not important.
    </p>

    <h2>The Dirt</h2>
    <p>
        This is not a bit. Hark Horning is a genuinely credentialed specialist in the dirt industry — an industry that is, despite all appearances, very real and very serious. Dirt, it turns out, is one of the most consequential materials on the planet, and Hark spent years at the top of it (professionally speaking). He developed expertise in soil composition, earthwork specification, and the kind of nuanced material knowledge that most people don't know they're missing until they're standing in the wrong dirt. Colleagues respected him. Clients trusted him. The dirt community knew his name. This is all true.
    </p>

    <h2>The Software</h2>
    <p>
        Eventually, Hark traded the earth beneath his feet for a keyboard, and became a software engineer who builds things that work, then refactors them until they work more elegantly. He believes in clean code, structured logging, and never hardcoding passwords — a lesson learned once, never repeated.
    </p>

    <h2>Everything Else</h2>
    <p>
        Hark has also coached rock climbing, among various other pursuits too numerous and frankly too impressive to list here without making this page feel excessive. He is very humble about all of it.
    </p>

    <p class="closing">
        This site exists to share his art with the world. If you're here, congratulations — you have excellent taste.
    </p>
</div>

